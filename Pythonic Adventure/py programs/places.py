print("Here's the original list of places:")
places = ["alps", "zurich", "queenstown", "santiago bernabeu"]
print(places)

print("****************************************")
print("Here's the list sorted alphabetically:")
sorted_places = sorted(places)
print(sorted_places)

print("****************************************")
print("Here's the same list reverse sorted:")
reverse_sorted = sorted(places, reverse=True)
print(reverse_sorted)
